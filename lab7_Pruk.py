import random

# Отримуємо значення n від користувача
try:
    n = int(input("Введіть кількість елементів n: "))
    if n <= 0:
        print("Кількість елементів має бути більшою за 0.")
        exit()
except ValueError:
    print("Будь ласка, введіть ціле число.")
    exit()

# а) дійсні числа від 0 до 1 (від 0.0 до 1.0, не включаючи 1.0)
